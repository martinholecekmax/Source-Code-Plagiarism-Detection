public class MappedPathException extends Exception {
	
	private static final long serialVersionUID = -3387213328588027059L;
	public MappedPathException() {
		super();
			}
	public MappedPathException(String arg0, Throwable arg1) {
		super(arg0, arg1);
			}
	public MappedPathException(String arg0) {
		super(arg0);
			}
	public MappedPathException(Throwable arg0) {
		super(arg0);
			}
}